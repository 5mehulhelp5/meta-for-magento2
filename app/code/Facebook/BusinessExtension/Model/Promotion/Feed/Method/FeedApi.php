namespace Facebook\BusinessExtension\Model\Promotion\Feed\Method;

use Exception;
use Facebook\BusinessExtension\Helper\FBEHelper;
use Facebook\BusinessExtension\Helper\GraphAPIAdapter;
use Facebook\BusinessExtension\Model\System\Config as SystemConfig;
use Magento\Catalog\Model\ResourceModel\Category\Collection as CategoryCollection;
use Magento\Catalog\Model\ResourceModel\Product\Collection as ProductCollection;
use Magento\SalesRule\Model\ResourceModel\Rule\Collection as RuleCollection;

use Magento\Framework\App\Filesystem\DirectoryList;
use Magento\Framework\Exception\FileSystemException;
use Magento\Framework\Exception\LocalizedException;
use Magento\Framework\Exception\NoSuchEntityException;
use Magento\Framework\Filesystem;
use Magento\Framework\Filesystem\File\WriteInterface;
use Psr\Log\LoggerInterface;
use Facebook\BusinessExtension\Model\Promotion\Feed\Builder;
use Facebook\BusinessExtension\Model\Promotion\Feed\PromotionRetriever\PromotionRetriever;
use Magento\SalesRule\Model\RuleFactory;
use Magento\SalesRule\Api\Data\RuleInterface;
use Magento\SalesRule\Api\Data\ConditionInterface;
use Magento\SalesRule\Model\Rule\Condition\Product\Found;
use Magento\SalesRule\Model\Rule;

class FeedApi
{
    const FEED_FILE_NAME = 'facebook_promotions%s.csv';
    const FB_FEED_NAME = 'Magento Autogenerated Promotion Feed';


    protected $storeId;

    /**
     * @var SystemConfig
     */
    protected $systemConfig;

    /**
     * @var GraphAPIAdapter
     */
    protected $graphApiAdapter;

    /**
     * @var Filesystem
     */
    protected $fileSystem;

    /**
     * @var FBEHelper
     */
    protected $_fbeHelper;

    /**
     * @var LoggerInterface
     */
    private $logger;

    /**
     * @var Builder
     */
    protected $builder;


    protected $promotionRetriever;


    protected $ruleCollection;

    protected $ruleFactory;


    /**
     * @param SystemConfig $systemConfig
     * @param GraphAPIAdapter $graphApiAdapter
     * @param Filesystem $filesystem
     * @param FBEHelper $fbeHelper
     * @param Builder $builder
     * @param LoggerInterface $logger
     * @param PromotionRetriever $promotionRetriever
     * @param RuleCollection $ruleCollection
     * @param RuleFactory $ruleFactory
     */
    public function __construct(
        SystemConfig       $systemConfig,
        GraphAPIAdapter    $graphApiAdapter,
        Filesystem         $filesystem,
        FBEHelper          $fbeHelper,
        Builder            $builder,
        LoggerInterface    $logger,
        PromotionRetriever $promotionRetriever,

        RuleCollection     $ruleCollection,
        RuleFactory        $ruleFactory
    )
    {
        $this->systemConfig = $systemConfig;
        $this->graphApiAdapter = $graphApiAdapter;
        $this->fileSystem = $filesystem;
        $this->_fbeHelper = $fbeHelper;
        $this->ruleCollection = $ruleCollection;
        $this->builder = $builder;
        $this->logger = $logger;
        $this->promotionRetriever = $promotionRetriever;
        $this->ruleFactory = $ruleFactory;
    }

    /**
     * @return string|false
     */
    protected function getFbFeedId()
    {
        $feedId = $this->systemConfig->getOffersFeedId($this->storeId);
        $feedName = self::FB_FEED_NAME;

        if (!$feedId) {
            $catalogId = $this->systemConfig->getCatalogId($this->storeId);
            $offerFeeds = $this->graphApiAdapter->getOfferFeeds($catalogId);
            $magentoFeeds = array_filter($offerFeeds, function ($a) use ($feedName) {
                return $a['name'] === $feedName;
            });
            if (!empty($magentoFeeds)) {
                $feedId = $magentoFeeds[0]['id'];
            }
        }

        if (!$feedId) {
            $catalogId = $this->systemConfig->getCatalogId($this->storeId);
            $feedId = $this->graphApiAdapter->createEmptyFeed($catalogId, $feedName, true);

            $maxAttempts = 5;
            $attempts = 0;
            do {
                $feedData = $this->graphApiAdapter->getFeed($feedId);
                if ($feedData !== false) {
                    break;
                }
                $attempts++;
                sleep(2);
            } while ($attempts < $maxAttempts);
        }

        if (!$this->systemConfig->getFeedId($this->storeId) && $feedId) {
            $this->systemConfig->saveConfig(SystemConfig::XML_PATH_FACEBOOK_BUSINESS_EXTENSION_OFFERS_FEED_ID, $feedId, $this->storeId)
                ->cleanCache();
        }
        return $feedId;
    }

    /**
     * @param WriteInterface $fileStream
     * @throws FileSystemException
     * @throws Exception
     */
    protected function writeFile(WriteInterface $fileStream)
    {
        $fileStream->writeCsv($this->builder->getHeaderFields());

        $total = 0;
        $offers = $this->promotionRetriever->retrieve();
        foreach ($offers as $offer) {
            try {
                $offer->loadCouponCode();
                $entry = array_values($this->builder->buildPromoEntry($offer));
                $fileStream->writeCsv($entry);
                $total++;
            } catch (Exception $e) {
                $this->logger->debug(sprintf('Skipped promo: %s', $offer->getName()));
                $this->logger->debug($e);
            }
        }
        $this->logger->debug(sprintf('Generated feed with %d promos.', $total));
    }

    /**
     * Get file name with store code suffix for non-default store (no suffix for default one)
     *
     * @return string
     * @throws NoSuchEntityException
     */
    protected function getFeedFileName()
    {
        $defaultStoreId = $this->systemConfig->getStoreManager()->getDefaultStoreView()->getId();
        $storeCode = $this->systemConfig->getStoreManager()->getStore($this->storeId)->getCode();
        return sprintf(
            self::FEED_FILE_NAME,
            ($this->storeId && $this->storeId !== $defaultStoreId) ? ('_' . $storeCode) : ''
        );
    }

    /**
     * @return string
     * @throws FileSystemException
     * @throws NoSuchEntityException
     */
    protected function generatePromoFeed()
    {
        $file = 'export/' . $this->getFeedFileName();
        $directory = $this->fileSystem->getDirectoryWrite(DirectoryList::VAR_DIR);
        $directory->create('export');

        $stream = $directory->openFile($file, 'w+');
        $stream->lock();
        $this->writeFile($stream);
        $stream->unlock();


        return $directory->getAbsolutePath($file);
    }

    /**
     * @param null $storeId
     * @return bool|mixed
     * @throws Exception
     */
    public function execute($storeId = null)
    {
        $this->storeId = $storeId;
        $this->builder->setStoreId($this->storeId);
        $this->graphApiAdapter->setDebugMode($this->systemConfig->isDebugMode($storeId))
            ->setAccessToken($this->systemConfig->getAccessToken($storeId));

        try {
            $feedId = $this->getFbFeedId();
            if (!$feedId) {
                throw new LocalizedException(__('Cannot fetch feed ID'));
            }
            $feed = $this->generatePromoFeed();
            $this->logger->debug(sprintf('this is the feed %s!', $feed));


            return $this->graphApiAdapter->pushFeed($feedId, $feed);
        } catch (Exception $e) {
            $this->_fbeHelper->logException($e);
            throw $e;
        }
    }
}
